            <h2>RÈGLES DU JEU</h2>

                  <div class="introduction">
                    <p>Dans <span class="text_red">Paper Dungeons</span>, chaque joueur contrôle un groupe de héros en quête de gloire et de fortune dans un univers médiéval fantastique. Leur mission : traverser un donjon rempli d'ennemis et de pièges ! Au cours des huit manches de la partie, ils pourront augmenter leurs compétences, combattre de terribles monstres flanqués de sinistres sbires, et mettre la main sur d'innombrables trésors.</p>
                    <p>À la fin de la partie, chaque groupe de héros retourne au village pour raconter ses aventures à la taverne. Le groupe qui a vécu la plus formidable épopée et obtenu le plus de points permet au joueur qui le contrôle de remporter la victoire !</p>
